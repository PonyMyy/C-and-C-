#include<stdio.h>
int main()
//企业发放的奖金根据利润提成
{
	int l,g;     //利润l,奖金g
	printf("请输入利润：\n");
	scanf("%d",&l);
	printf("应发奖金为：\n");
	if(l>1000000)
	 g=100000*0.1+100000*0.075+200000*0.05+200000*0.03+400000*0.015+(l-1000000)*0.01;
	 else 
	 {
	  if(l>600000&&l<=1000000)
	  g=100000*0.1+100000*0.075+200000*0.05+200000*0.03+(l-600000)*0.015;
	  else 
	  {
	   if(l>400000&&l<=600000)
	   g=100000*0.1+100000*0.075+200000*0.05+(l-400000)*0.03;
	   else 
	      {
	       if(l>200000&&l<=400000)
	       g=100000*0.1+100000*0.075+(l-200000)*0.05;
	       else 
	          {
	           if(l>100000&&l<=200000)
	           g=100000*0.1+(l-100000)*0.075;
	           else
	           g=l*0.1;
	          }
	       }
	   }
     }
	 printf("%d\n",g);
	 return 0;
 } 
